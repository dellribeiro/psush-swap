#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}

/*
Description
The functio writes a string specified by the argument
on file descriptor.

Return Value
Outputs the string ’s’ to the given file
descriptor.
*/
