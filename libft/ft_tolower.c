#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		c += 32;
	return (c);
}

/*
Description 
The function tolower converts a given
letter to lowercase.

Return Value 
This function returns lowercase equivalent to c,
if such value exists, else c remains unchanged.
*/