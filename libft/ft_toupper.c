#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		c -= 32;
	return (c);
}

/*
Description 
The function toupper converts lowercase
letter to uppercase.

Return Value 
This function returns uppercase equivalent to c,
if such value exists, else c remains unchanged.
*/